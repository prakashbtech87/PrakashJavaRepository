package com.prakash.collections.treeset;

import java.util.TreeSet;

public class TreeSetAlphabetical {

	public static void main(String[] args) {
		TreeSet<String> ts = new TreeSet<>();
		ts.add(new String("K"));
		ts.add(new String("Z"));
		ts.add(new String("P"));
		ts.add(new String("a"));
		ts.add(new String("S"));
		ts.add(new String("A"));
		System.out.println(ts);
	}

}
